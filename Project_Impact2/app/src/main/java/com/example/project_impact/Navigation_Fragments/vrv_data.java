package com.example.project_impact.Navigation_Fragments;

public class vrv_data {
    String name;
    int image;

    public vrv_data(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
